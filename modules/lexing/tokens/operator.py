from .token import Token

class Operator(Token):
    LIST = (".", "->", "++", "--", "%", "!",
            "^", "@", "-", "~", "~<", "~>", "*",
            "**", "/", "+", "<<", ">>", "&", "|", "$",
            "<", ">", "<=", ">=", "==", "!=", "<=>",
            "=", "**=", "*=", "/=", "%=", "+=", "-=",
            "<<=", ">>=", "&=", "|=", "$="
    )

    SYMBOLS = {c for op in LIST for c in op }
